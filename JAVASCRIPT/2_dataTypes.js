// Data Types:
// Premetive data types: number (integer/float), string("hello$%&"), boolean(true/false),
// undefined(when you declared variable,but didn't assign value), null
// non premetive/ Complex Data types: array,object,function,JSON(Javascript Object Notation)

// how to check data type -> typeof
console.log(typeof '1234'); // string
console.log(typeof 12.34); // number
console.log(typeof "Hello"); // string
console.log(typeof "hello$%&"); // string
console.log(typeof true); // boolean